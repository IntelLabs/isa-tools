ASL_bits2048_t
ASL_lsl_bits_2048(int width, ASL_bits2048_t x, ASL_int_t d)
{
        if (d == 0)
                return x;
        if (d < 64) {
                x.u64[31] = (x.u64[30] >> (64 - d)) | (x.u64[31] << d);
                x.u64[30] = (x.u64[29] >> (64 - d)) | (x.u64[30] << d);
                x.u64[29] = (x.u64[28] >> (64 - d)) | (x.u64[29] << d);
                x.u64[28] = (x.u64[27] >> (64 - d)) | (x.u64[28] << d);
                x.u64[27] = (x.u64[26] >> (64 - d)) | (x.u64[27] << d);
                x.u64[26] = (x.u64[25] >> (64 - d)) | (x.u64[26] << d);
                x.u64[25] = (x.u64[24] >> (64 - d)) | (x.u64[25] << d);
                x.u64[24] = (x.u64[23] >> (64 - d)) | (x.u64[24] << d);
                x.u64[23] = (x.u64[22] >> (64 - d)) | (x.u64[23] << d);
                x.u64[22] = (x.u64[21] >> (64 - d)) | (x.u64[22] << d);
                x.u64[21] = (x.u64[20] >> (64 - d)) | (x.u64[21] << d);
                x.u64[20] = (x.u64[19] >> (64 - d)) | (x.u64[20] << d);
                x.u64[19] = (x.u64[18] >> (64 - d)) | (x.u64[19] << d);
                x.u64[18] = (x.u64[17] >> (64 - d)) | (x.u64[18] << d);
                x.u64[17] = (x.u64[16] >> (64 - d)) | (x.u64[17] << d);
                x.u64[16] = (x.u64[15] >> (64 - d)) | (x.u64[16] << d);
                x.u64[15] = (x.u64[14] >> (64 - d)) | (x.u64[15] << d);
                x.u64[14] = (x.u64[13] >> (64 - d)) | (x.u64[14] << d);
                x.u64[13] = (x.u64[12] >> (64 - d)) | (x.u64[13] << d);
                x.u64[12] = (x.u64[11] >> (64 - d)) | (x.u64[12] << d);
                x.u64[11] = (x.u64[10] >> (64 - d)) | (x.u64[11] << d);
                x.u64[10] = (x.u64[9]  >> (64 - d)) | (x.u64[10] << d);
                x.u64[9]  = (x.u64[8]  >> (64 - d)) | (x.u64[9] << d);
                x.u64[8]  = (x.u64[7]  >> (64 - d)) | (x.u64[8] << d);
                x.u64[7]  = (x.u64[6]  >> (64 - d)) | (x.u64[7] << d);
                x.u64[6]  = (x.u64[5]  >> (64 - d)) | (x.u64[6] << d);
                x.u64[5]  = (x.u64[4]  >> (64 - d)) | (x.u64[5] << d);
                x.u64[4]  = (x.u64[3]  >> (64 - d)) | (x.u64[4] << d);
                x.u64[3]  = (x.u64[2]  >> (64 - d)) | (x.u64[3] << d);
                x.u64[2]  = (x.u64[1]  >> (64 - d)) | (x.u64[2] << d);
                x.u64[1]  = (x.u64[0]  >> (64 - d)) | (x.u64[1] << d);
                x.u64[0]  = x.u64[0] << d;
        } else {
                x.u64[31] = x.u64[30];
                x.u64[30] = x.u64[29];
                x.u64[29] = x.u64[28];
                x.u64[28] = x.u64[27];
                x.u64[27] = x.u64[26];
                x.u64[26] = x.u64[25];
                x.u64[25] = x.u64[24];
                x.u64[24] = x.u64[23];
                x.u64[23] = x.u64[22];
                x.u64[22] = x.u64[21];
                x.u64[21] = x.u64[20];
                x.u64[20] = x.u64[19];
                x.u64[19] = x.u64[18];
                x.u64[18] = x.u64[17];
                x.u64[17] = x.u64[16];
                x.u64[16] = x.u64[15];
                x.u64[15] = x.u64[14];
                x.u64[14] = x.u64[13];
                x.u64[13] = x.u64[12];
                x.u64[12] = x.u64[11];
                x.u64[11] = x.u64[10];
                x.u64[10] = x.u64[9];
                x.u64[9]  = x.u64[8];
                x.u64[8]  = x.u64[7];
                x.u64[7]  = x.u64[6];
                x.u64[6]  = x.u64[5];
                x.u64[5]  = x.u64[4];
                x.u64[4]  = x.u64[3];
                x.u64[3]  = x.u64[2];
                x.u64[2]  = x.u64[1];
                x.u64[1]  = x.u64[0];
                x.u64[0]  = 0;
                x = ASL_lsl_bits_2048(width, x, d - 64);
        }
        return ASL_and_bits_2048(width, x, ASL_mk_mask_2048(width));
}

ASL_bits2048_t
ASL_lsr_bits_2048(int width, ASL_bits2048_t x, ASL_int_t d)
{
        if (d == 0)
                return x;
        if (d < 64) {
                x.u64[0]  = (x.u64[1]  << (64 - d)) | (x.u64[0] >> d);
                x.u64[1]  = (x.u64[2]  << (64 - d)) | (x.u64[1] >> d);
                x.u64[2]  = (x.u64[3]  << (64 - d)) | (x.u64[2] >> d);
                x.u64[3]  = (x.u64[4]  << (64 - d)) | (x.u64[3] >> d);
                x.u64[4]  = (x.u64[5]  << (64 - d)) | (x.u64[4] >> d);
                x.u64[5]  = (x.u64[6]  << (64 - d)) | (x.u64[5] >> d);
                x.u64[6]  = (x.u64[7]  << (64 - d)) | (x.u64[6] >> d);
                x.u64[7]  = (x.u64[8]  << (64 - d)) | (x.u64[7] >> d);
                x.u64[8]  = (x.u64[9]  << (64 - d)) | (x.u64[8] >> d);
                x.u64[9]  = (x.u64[10] << (64 - d)) | (x.u64[9] >> d);
                x.u64[10] = (x.u64[11] << (64 - d)) | (x.u64[10] >> d);
                x.u64[11] = (x.u64[12] << (64 - d)) | (x.u64[11] >> d);
                x.u64[12] = (x.u64[13] << (64 - d)) | (x.u64[12] >> d);
                x.u64[13] = (x.u64[14] << (64 - d)) | (x.u64[13] >> d);
                x.u64[14] = (x.u64[15] << (64 - d)) | (x.u64[14] >> d);
                x.u64[15] = (x.u64[16] << (64 - d)) | (x.u64[15] >> d);
                x.u64[16] = (x.u64[17] << (64 - d)) | (x.u64[16] >> d);
                x.u64[17] = (x.u64[18] << (64 - d)) | (x.u64[17] >> d);
                x.u64[18] = (x.u64[19] << (64 - d)) | (x.u64[18] >> d);
                x.u64[19] = (x.u64[20] << (64 - d)) | (x.u64[19] >> d);
                x.u64[20] = (x.u64[21] << (64 - d)) | (x.u64[20] >> d);
                x.u64[21] = (x.u64[22] << (64 - d)) | (x.u64[21] >> d);
                x.u64[22] = (x.u64[23] << (64 - d)) | (x.u64[22] >> d);
                x.u64[23] = (x.u64[24] << (64 - d)) | (x.u64[23] >> d);
                x.u64[24] = (x.u64[25] << (64 - d)) | (x.u64[24] >> d);
                x.u64[25] = (x.u64[26] << (64 - d)) | (x.u64[25] >> d);
                x.u64[26] = (x.u64[27] << (64 - d)) | (x.u64[26] >> d);
                x.u64[27] = (x.u64[28] << (64 - d)) | (x.u64[27] >> d);
                x.u64[28] = (x.u64[29] << (64 - d)) | (x.u64[28] >> d);
                x.u64[29] = (x.u64[30] << (64 - d)) | (x.u64[29] >> d);
                x.u64[30] = (x.u64[31] << (64 - d)) | (x.u64[30] >> d);
                x.u64[31] = x.u64[31] >> d;
        } else {
                x.u64[0]  = x.u64[1];
                x.u64[1]  = x.u64[2];
                x.u64[2]  = x.u64[3];
                x.u64[3]  = x.u64[4];
                x.u64[4]  = x.u64[5];
                x.u64[5]  = x.u64[6];
                x.u64[6]  = x.u64[7];
                x.u64[7]  = x.u64[8];
                x.u64[8]  = x.u64[9];
                x.u64[9]  = x.u64[10];
                x.u64[10] = x.u64[11];
                x.u64[11] = x.u64[12];
                x.u64[12] = x.u64[13];
                x.u64[13] = x.u64[14];
                x.u64[14] = x.u64[15];
                x.u64[15] = x.u64[16];
                x.u64[16] = x.u64[17];
                x.u64[17] = x.u64[18];
                x.u64[18] = x.u64[19];
                x.u64[19] = x.u64[20];
                x.u64[20] = x.u64[21];
                x.u64[21] = x.u64[22];
                x.u64[22] = x.u64[23];
                x.u64[23] = x.u64[24];
                x.u64[24] = x.u64[25];
                x.u64[25] = x.u64[26];
                x.u64[26] = x.u64[27];
                x.u64[27] = x.u64[28];
                x.u64[28] = x.u64[29];
                x.u64[29] = x.u64[30];
                x.u64[30] = x.u64[31];
                x.u64[31] = 0;
                x = ASL_lsr_bits_2048(width, x, d - 64);
        }
        return x;
}

